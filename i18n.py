def translate_html(html, lang):
    """Translate simple rendered text/attribute nodes without touching code blocks."""
    if lang == DEFAULT_LANGUAGE:
        return html

    import re

    protected = []

    def stash(match):
        protected.append(match.group(0))
        return f'<i18n-protected data-index="{len(protected) - 1}"></i18n-protected>'

    html = re.sub(
        r"<(script|style|pre|code|svg)\b[\s\S]*?</\1>",
        stash,
        html,
        flags=re.IGNORECASE,
    )

    translations = UI_TRANSLATIONS.get(lang, {})

    def translate_text_match(match):
        text = match.group(1)
        stripped = text.strip()
        if not stripped:
            return match.group(0)
        translated = translate_ui(stripped, lang)
        if translated == stripped:
            return match.group(0)
        leading = text[: len(text) - len(text.lstrip())]
        trailing = text[len(text.rstrip()) :]
        return ">" + leading + translated + trailing + "<"

    html = re.sub(r">([^<>]+)<", translate_text_match, html)

    def translate_attr_match(match):
        attr, quote, value = match.groups()
        translated = translate_ui(value, lang)
        if translated == value:
            return match.group(0)
        return f'{attr}={quote}{translated}{quote}'

    html = re.sub(
        r"\b(title|placeholder|aria-label|alt)=([\"'])(.*?)\2",
        translate_attr_match,
        html,
        flags=re.IGNORECASE,
    )

    for index, block in enumerate(protected):
        html = html.replace(
            f'<i18n-protected data-index="{index}"></i18n-protected>',
            block,
        )
    return html

