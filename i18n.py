def translate_chat_error(message, lang):
    """Translate known chatbot errors locally, preserving unknown technical details."""
    if not message:
        return message

    target_index = 1 if normalize_language(lang) == "it" else 0
    source_index = 1 - target_index

    for pair in CHAT_ERROR_TRANSLATIONS:
        if message == pair[target_index]:
            return message
        if message == pair[source_index]:
            return pair[target_index]

    for pair in CHAT_ERROR_PREFIX_TRANSLATIONS:
        target_matches = message.startswith(pair[target_index])
        source_matches = message.startswith(pair[source_index])
        if source_matches and (
            not target_matches
            or len(pair[source_index]) > len(pair[target_index])
        ):
            return pair[target_index] + message[len(pair[source_index]):]
        if target_matches:
            return message

    return translate_server_message(message, lang)


