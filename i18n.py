def normalize_language(value):
    return value if value in LANGUAGES else DEFAULT_LANGUAGE


def translate_ui(text, lang):
    translations = UI_TRANSLATIONS.get(lang, {})
    if text in translations:
        return translations[text]

    import html
    import re
    normalized = re.sub(r"\s+", " ", str(text or "")).strip()
    if normalized in translations:
        return translations[normalized]
    decoded = html.unescape(normalized)
    return translations.get(decoded, text)


def translate_server_message(message, lang):
    if not message:
        return message
    if lang == "en":
        if message in SERVER_TRANSLATIONS_TO_EN:
            return SERVER_TRANSLATIONS_TO_EN[message]
        for prefix, translated in PREFIX_TRANSLATIONS_TO_EN.items():
            marker = f"{prefix}:"
            if message.startswith(marker):
                return translated + message[len(prefix):]
        return message

    if message in SERVER_TRANSLATIONS:
        return SERVER_TRANSLATIONS[message]

    for prefix, translated in PREFIX_TRANSLATIONS.items():
        marker = f"{prefix}:"
        if message.startswith(marker):
            return translated + message[len(prefix):]

    return message
